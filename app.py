    "import os\n",
    "import streamlit as st\n",
    "import pandas as pd\n",
    "import matplotlib.pyplot as plt\n",
    "import seaborn as sns\n",
    "from docx import Document\n",
    "from docx.shared import Inches\n",
    "\n",
    "# Create tmp folder\n",
    "os.makedirs(\"tmp\", exist_ok=True)\n",
